var threeSum = function (nums) {
  const solutionsSet = new Set();
  const solutionsArray = [];
  // loop through nums for firstPointer
  for (let firstP = 0; firstP < nums.length - 2; firstP++) {
    // loop through nums for secondPointer
    for (let secondP = firstP + 1; secondP < nums.length - 1; secondP++) {
      // thirdPointerSet testing
      const thirdPointerSet = new Set(nums.slice(secondP + 1));
      // find desiredNum
      const desiredNum = -(nums[firstP] + nums[secondP]);
      if (thirdPointerSet.has(desiredNum)) {
        // test if thirdP is the desiredNum
        const solutionFound = [nums[firstP], nums[secondP], desiredNum]
          .sort()
          .join();
        // test if in solutionsSet
        if (!solutionsSet.has(solutionFound)) {
          solutionsSet.add(solutionFound);
          solutionsArray.push([nums[firstP], nums[secondP], desiredNum]);
        }
      }
    }
  }
  return solutionsArray;
};

